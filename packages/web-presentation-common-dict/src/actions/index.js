const reqError = error => {
    let number = undefined;
    let text = undefined;
    let data = undefined;

    switch (error.status) {
        case 404:
            number = 404;
            text = 'Запись не найдена';
            data = error.response.body;
            break;
        default:
            text = 'Неизвестная ошибка'
    }

    return {
        error: {
            number: number,
            text: text,
            data: data
        }
    }
};

