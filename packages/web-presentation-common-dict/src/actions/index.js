const GetBranchByCode = ({RestClient}, code) => {
    let delimiterIndex = code.indexOf(":");
    if (delimiterIndex >= 0) {
        //если передали systemId вида BISQUIT:6300, то отрезаем название системы и ищем по 6300
        code = code.substr(delimiterIndex + 1);
    }
    return RestClient
        .get(`/dictionaries/branches/code=${code}`)
        .then(response => response.body)
};

